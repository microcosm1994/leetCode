/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var findMaxAverage = function (nums, k) {
    let result = []
    for (let i = 0; i <= nums.length - k; i++) {
        let num = 0
        for (let j = i; j < i + k; j++) {
            num += nums[j]
        }
        result.push(num / k)
    }
    console.log(result)
    result.sort((a, b) => b - a)
    return result[0]
};
